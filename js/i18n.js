const I18N = (() => {
  let lang = localStorage.getItem('kit_lang') || 'ru';

  function get() { return lang; }

  function t(key) {
    return TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS.ru[key] ?? key;
  }

  function set(newLang) {
    if (!TRANSLATIONS[newLang]) return;
    lang = newLang;
    localStorage.setItem('kit_lang', lang);
    apply();
    // Call page re-render if registered
    if (typeof window._rerender === 'function') window._rerender();
  }

  function apply() {
    const cfg = TRANSLATIONS[lang];
    document.documentElement.lang = lang;
    document.documentElement.dir = cfg.dir;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      if (key) el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      el.placeholder = t(el.dataset.i18nPh);
    });
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === lang);
    });
  }

  return { get, t, set, apply };
})();

function t(key) { return I18N.t(key); }
